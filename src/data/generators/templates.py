def get_template(language: str, length_category: str, rng) -> str:
    """
    Get a random template for the specified language and length category.
    
    Args:
        language: Language code
        length_category: 'short', 'medium', or 'long'
        rng: Random number generator
        
    Returns:
        Template string with placeholders
    """
    if language not in TEMPLATES:
        raise ValueError(f"Unknown language: {language}")
    
    if length_category not in TEMPLATES[language]:
        raise ValueError(f"Unknown length category: {length_category}")
    
    return rng.choice(TEMPLATES[language][length_category])
